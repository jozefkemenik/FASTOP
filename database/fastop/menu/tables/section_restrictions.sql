      (MENU_SID
,      SECTION_ID
,      USER_GROUP_SID
,      RESTRICTION_TYPE_SID)
,  CONSTRAINT MN_SECTION_RESTR_MENU_FK FOREIGN KEY
      (MENU_SID)
