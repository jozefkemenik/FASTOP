       REFERENCES UM_GROUPS (GROUP_SID)
,  CONSTRAINT MN_SECTION_RESTR_RT_FK FOREIGN KEY
      (RESTRICTION_TYPE_SID)
       REFERENCES MN_RESTRICTION_TYPES (SID)
