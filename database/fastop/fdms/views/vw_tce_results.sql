CREATE OR REPLACE FORCE VIEW VW_TCE_RESULTS
AS
    SELECT R.EXPORTER_CTY_ID
         , R.INDICATOR_SID
         , R.DATA_TYPE
         , R.START_YEAR
         , R.TIMESERIE_DATA
         , R.UPDATE_DATE
         , R.UPDATE_USER
         , I.INDICATOR_ID
         , G.DESCR AS COUNTRY_DESCR
         , I.DATA_SOURCE
         , I.PROVIDER_ID
         , M.ROUND_SID
         , M.STORAGE_SID
         , I.PERIODICITY_ID
      FROM FDMS_TCE_RESULTS  R
      JOIN FDMS_TCE_MATRICES M
        ON R.TCE_MATRIX_SID = M.TCE_MATRIX_SID
      JOIN VW_FDMS_INDICATORS I
        ON M.PROVIDER_SID = I.PROVIDER_SID AND R.INDICATOR_SID = I.INDICATOR_SID
      JOIN GEO_AREAS G
        ON G.GEO_AREA_ID = R.EXPORTER_CTY_ID;
