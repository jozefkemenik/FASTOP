DECLARE
    l_dataset_sid NUMBER(8);
    l_uploading_sid NUMBER(8);
    l_uploaded_sid NUMBER(8);
    l_validating_sid NUMBER(8);
    l_validated_sid NUMBER(8);
    l_publishing_sid NUMBER(8);
    l_published_sid NUMBER(8);
    l_finished_sid NUMBER(8);
    l_cancelled_sid NUMBER(8);
BEGIN

SELECT S.STATUS_SID INTO l_dataset_sid    FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'DATASET';
SELECT S.STATUS_SID INTO l_uploading_sid  FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'UPLOADING';
SELECT S.STATUS_SID INTO l_uploaded_sid   FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'UPLOADED';
SELECT S.STATUS_SID INTO l_validating_sid FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'VALIDATING';
SELECT S.STATUS_SID INTO l_validated_sid  FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'VALIDATED';
SELECT S.STATUS_SID INTO l_publishing_sid FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'PUBLISHING';
SELECT S.STATUS_SID INTO l_published_sid  FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'PUBLISHED';
SELECT S.STATUS_SID INTO l_finished_sid   FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'FINISHED';
SELECT S.STATUS_SID INTO l_cancelled_sid  FROM DSLOAD_STATUSES S WHERE S.STATUS_ID = 'CANCELLED';

-- DATASET
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_dataset_sid, l_uploading_sid);

-- UPLOADING
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploading_sid, l_uploaded_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploading_sid, l_dataset_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploading_sid, l_cancelled_sid);

-- UPLOADED
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploaded_sid, l_validating_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploaded_sid, l_uploading_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploaded_sid, l_dataset_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_uploaded_sid, l_cancelled_sid);

-- VALIDATING
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validating_sid, l_validated_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validating_sid, l_cancelled_sid);

-- VALIDATED
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validated_sid, l_publishing_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validated_sid, l_uploading_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validated_sid, l_validating_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validated_sid, l_dataset_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_validated_sid, l_cancelled_sid);

-- PUBLISHING
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_publishing_sid, l_published_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_publishing_sid, l_cancelled_sid);

-- PUBLISHED
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_published_sid, l_finished_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_published_sid, l_publishing_sid);
INSERT INTO DSLOAD_VALID_STATUSES (STATUS_SID,VALID_STATUS_SID) VALUES (l_published_sid, l_cancelled_sid);

COMMIT;

END;
