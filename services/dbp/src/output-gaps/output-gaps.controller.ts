import { EApps } from 'config'

import { LibOutputGapsController } from '../../../lib/dist/fisc/output-gaps/'

export class OutputGapsController extends LibOutputGapsController {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Instance Members ///////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    private readonly ratsInputIndicators: Record<string, string>

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Constructor ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    constructor(appId: EApps) {
        super(appId)

        this.ratsInputIndicators = Object.assign({}, super.getRatsInputIndicators(), {
            _G_INV: 'T01aL10',
            _BB: 'T01dL06',
            _OO: 'T02aL08',
            _INT: 'T04aL14',
        }, )
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Protected Methods //////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    /**********************************************************************************************
     * @method getRatsInputIndicators
     *********************************************************************************************/
    protected getRatsInputIndicators(): Record<string, string> {
        return this.ratsInputIndicators
    }
}
