import { AuthzService } from './authz.service'

export class AuthzController {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Instance Members ///////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Constructor ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    constructor(private authzService: AuthzService) { }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Public Methods /////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    /**********************************************************************************************
     * @method getAuthzCountries get authorisation countries
     *********************************************************************************************/
    public getAuthzCountries() {
        return this.authzService.getAuthzCountries().catch(err => {
            err.method = 'UserController.getAuthzCountries'
            throw err
        })
    }

    /**********************************************************************************************
     * @method getAuthzGroups get authorisation groups
     *********************************************************************************************/
    public getAuthzGroups() {
        return this.authzService.getAuthzGroups().catch(err => {
            err.method = 'UserController.getUserAuthzs'
            throw err
        })
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Private Methods ////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
}
