import { catchAll } from '../../../lib/dist/catch-decorator'

import { IImfData } from './shared-interfaces'
import { ImfService } from './imf.service'

@catchAll(__filename)
export class ImfController {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Instance Members ///////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Constructor ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public constructor(private imfService: ImfService) {}

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Public Methods /////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    /**********************************************************************************************
     * @method getImfData
     *********************************************************************************************/
    public async getImfData(
        dataset: string, queryKey: string, startPeriod: number, endPeriod: number,
    ): Promise<IImfData> {
        return this.imfService.getImfData(dataset, queryKey, startPeriod, endPeriod)
    }
}
