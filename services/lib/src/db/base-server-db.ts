import { BaseServer } from '../base-server'
import { ISchemaConfigs } from 'config'
import { LoggingService } from '..'

export abstract class BaseServerDb extends BaseServer {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Static Members /////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    protected static schemas: ISchemaConfigs

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////// Constructor ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    protected constructor(logs: LoggingService, version?: string) {
        super(logs, version)

        this.registerShutdownListeners()
    }
}
